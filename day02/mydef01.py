# 자바 method, 자바스크립트 function과 같음
# 파이썬 함수: def

def add(a,b):
    return a+b

def minus(a,b):
    return a-b

def multiply(a,b):
    return a*b

def divide(a,b):
    return a/b

sum = add(4,6)
min = minus(4,6)
mul = multiply(4,6)
div = divide(4,6)

print("+",sum)
print("-",min)
print("*",mul)
print("/",div)